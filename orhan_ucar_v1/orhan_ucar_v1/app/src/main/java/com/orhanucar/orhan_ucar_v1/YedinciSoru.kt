package com.orhanucar.orhan_ucar_v1

class YedinciSoru {

    /*  Class Nedir?
    Class (sınıf) kavramı, programlamada bir nesne yaratmak için kullanılan bir şablon veya kalıptır.
    Bir sınıf, nesnelerin özelliklerini ve bu özellikler üzerinde işlemleri tanımlayan bir yapıdır.
    Sınıflar, bir nesneyi tanımlayan özellikleri ve bu nesne üzerinde yapılacak işlemleri bir arada tutarak,
    nesnelerin oluşturulmasını, kullanılmasını ve yönetilmesini sağlar. Sınıflar, OOP'nin en önemli yapı taşlarından biridir
    ve birçok nesne yönelimli programlama dilinde kullanılır.
     */

    /*  Abstract Nedir?
    Abstract (Soyut) sınıflar, diğer sınıfların bazı özelliklerini kalıtım olarak alabilecekleri soyut bir şablon sağlarlar.
    Bir abstract sınıfın bir veya daha fazla abstract methodu olabilir, bu methodlar soyut yani içi boş olarak tanımlanır
    ve implement edilirken alt sınıflar tarafından doldurulurlar. Abstract sınıfların kendi başlarına örnekleri oluşturulamaz,
    ancak alt sınıflarından örnekler oluşturulabilir. Abstract sınıflar, sınıf hiyerarşisinde kalıtım yapısında kullanılarak
    genel özellikleri belirlemek ve tekrar kullanılabilirliği artırmak için kullanılır.
     */

    /*  Interface Nedir?
    Interface birbirinden bağımsız sınıfların belirli bir davranışı uygulamalarını sağlamak için kullanılan bir yapıdır.
    Bir interface, sınıfın uygulaması gereken metotların ve özelliklerin bir listesini tanımlar,
    ancak bu özelliklerin nasıl uygulanacağını belirtmez. Yani bir interface, sınıfın nasıl çalışacağına dair bir şablon sağlar.
    Sınıflar, bir interface'i uygulayarak, belirli bir davranışı benimseyebilir ve uygulayabilirler.
    Bu, kodun daha anlaşılır, esnek ve değiştirilebilir hale gelmesini sağlar.
     */

   /*   Class,Abstract ve Interface yapıları OOP de sık olarak kullanılan yapılardır.Kod tekrarını önlemeye
   temiz ve anlaşılabilir kod yazmaya yardımcı olur.Bu yapılar ile yazılan kodlar maliyeti azaltır kod bakımını kolaylaştırır.
    */
}